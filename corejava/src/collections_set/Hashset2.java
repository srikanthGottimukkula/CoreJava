package collections_set;

import java.util.*;

public class Hashset2 {

	public static void main(String[] args) {
	Set<String> str = new HashSet<String>();
	str.add("r");
	str.add("r");
	str.add("k");
	str.add("a");
	str.add("i");
	str.add("p");
	
	for (String string : str) {
		System.out.println(string);
	}

	}

}
